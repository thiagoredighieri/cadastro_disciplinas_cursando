    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        boolean cond;
        
         cond= verificarCampos();
         
         if (cond==false){
            objeto = new Disciplina();		
            objeto.setNome(disciplina.getText());
            objeto.setCarga_horaria(Double.parseDouble(carga_horaria.getText()));
            objeto.setCurso(curso.getText());
            objeto.setNum_vagas(Integer.parseInt(num_vagas.getText()));
            objeto.setPeriodo(Integer.parseInt(periodo.getText()));
            controle.salvar(objeto);
            Limpar();
            JOptionPane.showMessageDialog(rootPane, "Salvo com Sucesso");
         }else{
            JOptionPane.showMessageDialog(rootPane, "Os campos com * são obrigatórios");
         }
       
      
       
        
        
        
    }//GEN-LAST:event_SalvarActionPerformed

