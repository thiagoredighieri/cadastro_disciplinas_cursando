     public boolean verificarCampos(){
         
         boolean cond1=false, cond2=false, cond3=false, cond4=false, cond5=false;
       
         if (disciplina.getText().equalsIgnoreCase("")){
          
            cond1=true;
             
         }
         
         if (curso.getText().equalsIgnoreCase("")){
           
             cond2=true;
             
         }
         
         if (carga_horaria.getText().equalsIgnoreCase("")){
           
             cond3=true;
             
         }
         
         if (periodo.getText().equalsIgnoreCase("")){
            
             cond4=true;
             
         }
         
         if (num_vagas.getText().equalsIgnoreCase("")){
            
             cond5=true;
             
         }
         
         if (cond1==true||cond2==true||cond3==true||cond4==true||cond5==true){
             
              return true;
         }else{
             return false;
         }
         
        
      
     }


